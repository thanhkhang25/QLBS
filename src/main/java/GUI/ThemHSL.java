    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        // Lấy thông tin từ giao diện    
        try {
        // Lấy dữ liệu từ các ô nhập liệu
        String maHSL = txtMaHeSoLuong.getText().trim();
        String luongCoBanStr = txtLuongCoBan.getText().trim();
        String phuCapXangStr = txtPhuCapXangXe.getText().trim();
        String bhxStr = txtBHXH.getText().trim();
        String bhytStr = txtBHYT.getText().trim();
        String bhtnStr = txtBHTN.getText().trim();

        // Kiểm tra các trường bắt buộc không được để trống
        if(maHSL.isEmpty() || luongCoBanStr.isEmpty() || phuCapXangStr.isEmpty()
                || bhxStr.isEmpty() || bhytStr.isEmpty() || bhtnStr.isEmpty()){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
            return;
        }

        // Chuyển đổi các trường số sang kiểu double
        double luongCoBan = Double.parseDouble(luongCoBanStr);
        double phuCapXang = Double.parseDouble(phuCapXangStr);
        double bhx = Double.parseDouble(bhxStr);
        double bhyt = Double.parseDouble(bhytStr);
        double bhtn = Double.parseDouble(bhtnStr);

        // Kiểm tra giá trị hợp lệ: lương cơ bản phải lớn hơn 0 (các phụ cấp có thể = 0)
        if(luongCoBan <= 0){
            JOptionPane.showMessageDialog(this, "Lương cơ bản phải lớn hơn 0!");
            return;
        }

        // Tạo đối tượng HeSoLuong (DTO)
        // Giả sử constructor của HeSoLuong có dạng: 
        // HeSoLuong(String maHeSoLuong, double luongCoBan, double phuCapXang, double phuCapBHYT, double phuCapBHXH, double phuCapBHTN)
        DTO.HeSoLuong hsl = new DTO.HeSoLuong(maHSL, luongCoBan, phuCapXang, bhyt, bhx, bhtn);

        // Gọi BUS để thêm hệ số lương vào DB
        BUS.HeSoLuongBUS hslBUS = new BUS.HeSoLuongBUS();
        boolean success = hslBUS.addHeSoLuong(hsl);
        if(success){
            JOptionPane.showMessageDialog(this, "Thêm hệ số lương thành công!");
            added = true;
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Thêm hệ số lương thất bại. Kiểm tra lại dữ liệu.");
        }
    } catch(NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng số cho các trường lương và phụ cấp!");
    } catch(Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Có lỗi xảy ra: " + ex.getMessage());
    }

    }//GEN-LAST:event_btnThemActionPerformed

